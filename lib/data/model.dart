import 'package:flutter/material.dart';

class Employee {
  String? name;
  String? position;
  String? direction;
  String? department;
  String? email;
  String? birthDate;
  String? primaryPhoneNumber;
  String? officeNumber;
  String? whatsAppNumber;
  String? profileImage;

  Employee(
      this.name,
      this.position,
      this.direction,
      this.department,
      this.email,
      this.birthDate,
      this.primaryPhoneNumber,
      this.officeNumber,
      this.whatsAppNumber,
      this.profileImage);
}
